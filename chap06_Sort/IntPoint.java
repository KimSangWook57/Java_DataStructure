package chap06_Sort;

class IntPoint {

	private int ix;
	private int iy;

	public IntPoint(int x, int y) {
		ix = x;
		iy = y;
	}

	public int getX() {
		return ix;
	}

	public void setX(int x) {
		ix = x;
	}
	
	public int getY() {
		return iy;
	}
	
	public void setY(int y) {
		iy = y;
	}
}